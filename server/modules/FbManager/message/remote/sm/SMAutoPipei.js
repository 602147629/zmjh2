var SMAutoPipei = {};


SMAutoPipei.AutoPipeiReturn = function(result,obj,data,config){
    var obj;
    if(result == 1){
        //成功
        obj = {
            "result" : result,
            "gameKey" : obj.gameKey,
            "data" : data,
            "config" : config
        }
    }else{
        //失败
        obj = {
            "result" : result
        }
    }
    return obj;
}


module.exports = SMAutoPipei;